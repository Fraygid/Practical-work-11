for n in range(1,13):
    for k in range(1,13):
        m = 12 - n -k
        if m > 0:
            if 28 * n + 30 * k + 31 * m == 365:
                print(f'Месяцев с 28 днями: {n}, Месяцев с 30 днями: {k}, Месяцев с 31 днем: {m}')
                print(f'Проверка: 28*{n}+30*{k}+31*{m} = {28*n+30*k+31*m}')
            break
